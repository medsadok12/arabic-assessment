export async function POST(req) {
  const supabase = createClient();
  const { data: { user } } = await supabase.auth.getUser();
  if (!user) return NextResponse.json({ error: 'غير مخول' }, { status: 401 });

  let body;
  try { body = await req.json(); } catch {
    return NextResponse.json({ reply: 'تَفَضَّلْ يَا بَطَلُ، اسْأَلْنِي مَا تُرِيدُ!' });
  }

  const { message, history = [] } = body;
  if (!message?.trim()) return NextResponse.json({ reply: 'تَفَضَّلْ يَا بَطَلُ، اسْأَلْنِي مَا تُرِيدُ!' });

  const hist      = history.filter(m => m.text?.trim());
  const firstUser = hist.findIndex(m => m.role === 'user');
  const recent    = (firstUser > 0 ? hist.slice(firstUser) : hist).slice(-8);

  const anthropicKey = process.env.ANTHROPIC_API_KEY;
  const geminiKey    = process.env.GEMINI_API_KEY;

  // Diagnostic log — visible in Vercel function logs
  console.log('[faheem] keys present:', { anthropic: !!anthropicKey, gemini: !!geminiKey });

  // 1. Anthropic Claude
  if (anthropicKey) {
    try {
      const messages = [
        ...recent.map(m => ({ role: m.role === 'user' ? 'user' : 'assistant', content: m.text })),
        { role: 'user', content: message.trim() },
      ];
      const res = await fetch('https://api.anthropic.com/v1/messages', {
        method: 'POST',
        headers: {
          'x-api-key':         anthropicKey,
          'anthropic-version': '2023-06-01',
          'content-type':      'application/json',
        },
        body: JSON.stringify({
          model:      'claude-haiku-4-5-20251001',
          max_tokens: 300,
          system:     SYSTEM_PROMPT,
          messages,
        }),
      });
      if (!res.ok) {
        const err = await res.text();
        console.error('[faheem] Anthropic HTTP', res.status, err.slice(0, 200));
      } else {
        const json  = await res.json();
        const reply = json.content?.[0]?.text?.trim();
        if (reply) return NextResponse.json({ reply });
        console.error('[faheem] Anthropic empty reply', JSON.stringify(json).slice(0, 200));
      }
    } catch (e) {
      console.error('[faheem] Anthropic exception:', e?.message);
    }
  }

  // 2. Google Gemini
  if (geminiKey) {
    try {
      const contents = [
        ...recent.map(m => ({ role: m.role === 'user' ? 'user' : 'model', parts: [{ text: m.text }] })),
        { role: 'user', parts: [{ text: message.trim() }] },
      ];
      const res = await fetch(
        `https://generativelanguage.googleapis.com/v1beta/models/gemini-1.5-flash:generateContent?key=${geminiKey}`,
        {
          method: 'POST',
          headers: { 'Content-Type': 'application/json' },
          body: JSON.stringify({
            systemInstruction: { parts: [{ text: SYSTEM_PROMPT }] },
            contents,
            generationConfig: { maxOutputTokens: 300, temperature: 0.85, topP: 0.92 },
          }),
        }
      );
      if (!res.ok) {
        const err = await res.text();
        console.error('[faheem] Gemini HTTP', res.status, err.slice(0, 200));
      } else {
        const json  = await res.json();
        const reply = json.candidates?.[0]?.content?.parts?.[0]?.text?.trim();
        if (reply) return NextResponse.json({ reply });
        console.error('[faheem] Gemini empty reply', JSON.stringify(json).slice(0, 200));
      }
    } catch (e) {
      console.error('[faheem] Gemini exception:', e?.message);
    }
  }

  // 3. Local knowledge fallback
  if (!anthropicKey && !geminiKey) {
    console.warn('[faheem] No API keys configured — using local fallback. Add GEMINI_API_KEY or ANTHROPIC_API_KEY to Vercel env vars.');
  }
  return NextResponse.json({ reply: smartReply(message.trim()) });
}

/* ── Arabic text normaliser for robust pattern matching ── */
function norm(text) {
  return text
    .replace(/[ؐ-ًؚ-ٰٟۖ-ۭ]/g, '') // harakat
    .replace(/[أإآ]/g, 'ا') // أإآ → ا
    .replace(/ة/g, 'ه')               // ة → ه
    .replace(/ى/g, 'ي')               // ى → ي
    .replace(/\s+/g, ' ')
    .trim();
}

